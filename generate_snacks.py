# 2. Bing 图片异步爬虫 (最快、无限制、免费)
def fetch_image_bing(keyword):
    """使用 Bing 异步接口抓取图片直链"""
    print(f"🌍 正在极速抓取: {keyword}")
    # 构建搜索词，加入"包装"提高准确率
    search_query = quote(f"{keyword} 零食 包装")
    url = f"https://cn.bing.com/images/async?q={search_query}&first=1&count=5&mmasync=1"
    
    headers = {
        "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36"
    }
    
    try:
        res = requests.get(url, headers=headers, timeout=6)
        # Bing 的图片直链存放在 murl&quot;:&quot;URL&quot; 中
        img_urls = re.findall(r'murl&quot;:&quot;(.*?)&quot;', res.text)
        if img_urls:
            return img_urls[0] # 返回最相关的第一张图
    except Exception as e:
        print(f"❌ 抓取失败 {keyword}: {e}")
        
    return "https://via.placeholder.com/400x300?text=Image+Not+Found"

# 3. 数据展平与完善引擎
def build_database():
    final_db = []
    item_id = 1
    
    print("⏳ 开始构建 230+ 零食数据库并抓取真实图片...")
    
    for category, status_dict in raw_data.items():
        for status, snacks in status_dict.items():
            for snack_name in snacks:
                # 抓取真实网图
                real_image = fetch_image_bing(snack_name)
                
                # 自动生成详尽的介绍文字（修复前端点击空白的 bug）
                detail_desc = f"【{snack_name}】是属于「{category}」分类下的经典记忆。目前该商品处于【{status}】状态。无论是课间十分钟还是放学后的街角小卖部，它都承载了无数人不可磨灭的青春味道。"
                
                final_db.append({
                    "id": item_id,
                    "name": snack_name,
                    "category": category,
                    "status": status,
                    "tags": ["童年回忆", status, category.split('食品')[0]],
                    "image": real_image,
                    "detail_text": detail_desc # 保证详情页绝对有内容
                })
                item_id += 1
                
                # 极速爬虫也需要微小停顿，防止被防火墙拦截
                time.sleep(0.3) 
                
    return final_db

