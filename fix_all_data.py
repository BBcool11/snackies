def extract_brand(name):
    """从零食名称中提取品牌名"""
    # 优先匹配品牌映射表（按长度降序）
    sorted_items = sorted(BRAND_MAPPING.items(), key=lambda x: -len(x[0]))
    for keyword, brand in sorted_items:
        if keyword in name:
            return brand
    
    # 去除常见前缀
    clean = name
    for prefix in ['老版', '早期版', '新版', '经典版', '传统']:
        if clean.startswith(prefix):
            clean = clean[len(prefix):]
    
    # 去除常见后缀
    clean = re.sub(r'(老版|早期版|老包装|新包装|经典版|原味|香辣|麻辣|酸辣|番茄|芝士|海苔|烧烤|鸡肉|牛肉|排骨|海鲜|五连包|袋装|盒装|桶装|杯装|大包装|小包装|干脆面|方便面|虾条|薯片|雪饼|仙贝|奶糖|QQ糖|棒棒糖|果冻|饼干|蛋糕|派|巧克力|螺蛳粉|火锅|米线|自热|牛肉面|膨化|圈圈|老|版|包装|早期)$', '', clean)
    clean = clean.strip()
    
    # 取前2-4字作为品牌
    if len(clean) <= 2:
        return clean
    elif len(clean) == 3:
        return clean
    elif len(clean) == 4:
        return clean
    elif len(clean) <= 6:
        return clean[:4]
    else:
        return clean[:3]

def get_flavor_category(name, category):
    """判断口味分类"""
    text = (name + category).lower()
    
    for kw in FLAVOR_KEYWORDS["spicy"]:
        if kw in text:
            return "spicy"
    for kw in FLAVOR_KEYWORDS["sour"]:
        if kw in text:
            return "sour"
    for kw in FLAVOR_KEYWORDS["sweet"]:
        if kw in text:
            return "sweet"
    
    # 默认根据分类判断
    if any(x in category for x in ["糖", "巧克力", "饼干", "糕点", "冰淇淋", "奶"]):
        return "sweet"
    elif any(x in category for x in ["辣", "螺蛳粉", "火锅", "方便面"]):
        return "spicy"
    return "sweet"

def get_realistic_price(snack):
    """获取真实价格"""
    name = snack.get("name", "")
    
    # 优先匹配价格表
    for key, price in PRICE_FIXES.items():
        if key in name:
            return price
    
    # 根据分类给默认值
    category = snack.get("category", "")
    ranges = {
        "螺蛳粉": (10, 15), "方便面": (3, 8), "自热": (15, 35),
        "辣": (1, 8), "膨化": (5, 15), "薯片": (6, 12),
        "饼干": (5, 15), "糖果": (3, 15), "巧克力": (8, 40),
        "饮料": (2, 6), "坚果": (15, 35), "冰淇淋": (3, 15),
        "蜜饯": (5, 15),
    }
    
    for cat, (min_p, max_p) in ranges.items():
        if cat in category or cat in name:
            return round((min_p + max_p) / 2, 1)
    
    return 8.0  # 默认

